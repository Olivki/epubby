    fun isName(str: String?): Boolean = when (str) {
        null -> false
        else -> {
            val len = str.length
            when (len) {
                0 -> return false
                1 -> return isNameStart(str[0])
                else -> {
                    if (!isNameStart(str[0])) {
                        return false
                    }

                    var i = 1
                    while (i < len) {
                        if (!isNameTrail(str[i])) {
                            return false
                        }

                        i++
                    }
                }
            }
            true
        }
    }

    private fun StringBuilder.appendUHex(c: Int) {
        var c = c
        append('U')
        for (i in 0..5) {
            append(HEX_TABLE[c and 0xF00000 shr 20])
            c = c shl 4
        }
    }

    fun escapeName(str: String): String = buildString {
        var i = 0
        while (i < str.length) {
            val char = str[i]
            when {
                char.toInt() and 0xFC00 == 0xD800 -> {
                    val next = str[++i]
                    appendUHex((char.toInt() shl 10) + next.toInt() + SURROGATE_OFFSET)
                }
                i == 0 && !isNameStart(char) -> appendUHex(char.toInt())
                i != 0 && !isNameTrail(char) -> appendUHex(char.toInt())
                else -> append(char)
            }
            i++
        }
    }.intern()

